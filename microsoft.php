					<h3>Founding</h3>

					<p>Childhood friends Paul Allen and Bill Gates sought to make a successful business utilizing their shared skills in computer programming.[15] In 1972 they founded their first company, named Traf-O-Data, which sold a rudimentary computer to track and analyze automobile traffic data. While Gates enrolled at Harvard, Allen pursued a degree in computer science at Washington State University, though he later dropped out of school to work at Honeywell.[16] The January 1975 issue of Popular Electronics featured Micro Instrumentation and Telemetry Systems's (MITS) Altair 8800 microcomputer,[17] which inspired Allen to suggest that they could program a BASIC interpreter for the device. After a call from Gates claiming to have a working interpreter, MITS requested a demonstration. Since they didn't yet have one, Allen worked on a simulator for the Altair while Gates developed the interpreter. Although they developed the interpreter on a simulator and not the actual device, it worked flawlessly when they (in March 1975) demonstrated the interpreter to MITS in Albuquerque, New Mexico. MITS agreed to distribute it, marketing it as Altair BASIC.[14]:108, 112–114 Gates and Allen officially established Microsoft on April 4, 1975, with Gates as the CEO.[18] The original name of "Micro-Soft" was suggested by Allen.[19][20] In August 1977 the company formed an agreement with ASCII Magazine in Japan, resulting in its first international office, "ASCII Microsoft".[21] Microsoft moved to a new home in Bellevue, Washington in January 1979.</p>

					<p>Microsoft entered the operating system (OS) business in 1980 with its own version of Unix, called Xenix.[22] However, it was MS-DOS that solidified the company's dominance. After negotiations with Digital Research failed, IBM awarded a contract to Microsoft in November 1980 to provide a version of the CP/M OS, which was set to be used in the upcoming IBM Personal Computer (IBM PC).[23] For this deal, Microsoft purchased a CP/M clone called 86-DOS from Seattle Computer Products, which it branded as MS-DOS, though IBM rebranded it to PC DOS. Following the release of the IBM PC in August 1981, Microsoft retained ownership of MS-DOS. Since IBM had copyrighted the IBM PC BIOS, other companies had to reverse engineer it in order for non-IBM hardware to run as IBM PC compatibles, but no such restriction applied to the operating systems. Due to various factors, such as MS-DOS's available software selection, Microsoft eventually became the leading PC operating systems vendor.[24][25]:210 The company expanded into new markets with the release of the Microsoft Mouse in 1983, as well as with a publishing division named Microsoft Press.[14]:232 Paul Allen resigned from Microsoft in 1983 after developing Hodgkin's disease.[26] Allen claimed that Gates wanted to dilute his share in the company when he was diagnosed with Hodgkin’s disease because he didn’t think he was working hard enough.[27] After leaving Microsoft, Allen lost billions of dollars on ill-conceived or mistimed technology investments. He later invested in low-tech sectors, sports teams, and commercial real estate.</p>

